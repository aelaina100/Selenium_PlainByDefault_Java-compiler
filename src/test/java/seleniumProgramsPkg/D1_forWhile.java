package seleniumProgramsPkg;

// This "while" loop is the equivalent to "For" loop. One can replace another.
public class D1_forWhile {

	public static void main(String[] args) {
		
		int i=0;
		
		while (i<4)
		{
			System.out.println(" I should appear 4 times: 'while' loop");
			i++;
		}

	
	System.out.println(" ");
	int count =4;
	for (int j=0; j<count; j++)
	{
		System.out.println(" I should appear 4 times: 'For' loop");
	}
	
  }
}
